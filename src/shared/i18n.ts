export const translations = { 'zh-CN': zh, en };
export type TranslationKey = keyof typeof zh;
export type TranslationParams = Record<string, string | number>;

export function translate(language: AppLanguage, key: TranslationKey, params?: TranslationParams): string {
  const template = translations[normalizeLanguage(language)][key] ?? translations[defaultLanguage][key];
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (match, name: string) => String(params[name] ?? match));
}
